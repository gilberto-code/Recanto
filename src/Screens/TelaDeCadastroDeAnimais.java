    public Animal colherInformacao() throws SQLException, IOException {

        String nome;
        String especie;
        int idade;
        String raca;
        String cor;
        String porte;
        String sexo;
        String descricao;
        Date dataDeCadastro;
        String qualDoenca;
        //ImageIcon ImagemIcon;
        String StringImagem;
        boolean doente;
        boolean castrado;
        boolean vacinado;

        String imagemAnimal;
        //Coleta dos dados dos campos de texto
        nome = jtx_nome.getText();
        especie = jComboEspecie.getSelectedItem().toString();
        raca = jtx_raca.getText();
        cor = jtx_cor.getText();
        porte = (String) jComboPorte.getSelectedItem();

        if (jrMacho.isSelected()) {
            sexo = "Macho";
        } else {
            sexo = "F√™mea";
        }
        //sexo = cbSexoPress();
        descricao = jtx_descricao.getText();
        qualDoenca = jtxdoenca.getText();
        //Coleta de dados da barra 'slider'

        
        Calendar c = Calendar.getInstance();
        int dia = c.get(Calendar.DAY_OF_MONTH);
        int ano = c.get(Calendar.YEAR);
        int mes = c.get(Calendar.MONTH);
        //Coleta de dados da barra 'slider'

        String date = ano+"-"+(mes+1)+"-"+dia;
        System.out.println();
        java.sql.Date dat = java.sql.Date.valueOf(date);
        dataDeCadastro = dat;
        imagemAnimal = Controll_Images.ImagemParaString(ImagemIconAnimal);
        //System.out.println(imagemAnimal);
        
        vacinado = jrVacinadoSim.isSelected();

        doente = jrDoenteSim.isSelected();

        castrado = jrCastradoSim.isSelected();

        //idade = jSlider1.getValue();
        idade = 0;
        Animal animal = new Animal(nome, idade, especie, raca, 
                cor, porte, sexo,
                descricao,null, 
                qualDoenca, doente, castrado, vacinado, imagemAnimal);
        return animal;
    }

    public void adicionarImagem(JLabel fotinha) {
        JFileChooser jFileChooser1 = new javax.swing.JFileChooser(ultimoPacote);
        jFileChooser1.showOpenDialog(null);
        File arquivo = jFileChooser1.getSelectedFile();
        if (arquivo != null) {
            ultimoPacote = arquivo.getPath();
            ImageIcon imagem = new ImageIcon(ultimoPacote);
            fotinha.setIcon(redimensionar_imagem(imagem));
            ImagemIconAnimal = imagem;
        } else {
            System.out.println("Deu muito ruim");
        }
    }

    public ImageIcon redimensionar_imagem(ImageIcon img) {
        img.setImage(img.getImage().getScaledInstance(200, 150, 100));
        return img;
    }


